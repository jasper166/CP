const int INF = 1e9;
const int MOD = 1e9 + 7;
const int MAX = 505;

bool col[MAX], diag1[MAX], diag2[MAX];
int n, ways;

void search(int y){
    for (int x = 0; x < n; x++){
        if (y == n){
            ways++;
            return;
        }
        if (col[x] || diag1[x + y] || diag2[x - y + n - 1])
            continue;
        col[x] = diag1[x + y] = diag2[x - y + n - 1] = 1;
        search(y + 1);
        col[x] = diag1[x + y] = diag2[x - y + n - 1] = 0;        
    }
}

void run_case(){
    cin >> n;
    ways = 0;
    search(0);
    cout << ways << "\n";
}

