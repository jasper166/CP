vector <int> prime;
int d[MAX];
int n;
ll res;
void factorize (int n) {
    int N = n;
    for (int i = 2; i * i <= n; i++){
        if (N % i == 0) {
            prime.push_back(i);
            while (N % i == 0){
                d[i]++;
                N /= i;
            }
        }
    }

    if (N != 1){
        res *= N;
    }
}
void run_case(){
    cin >> n;
    res = n;
    factorize(n);
    for (auto x : prime) {
        if (d[x] & 1)
            res *= x;
    }
    cout << res << "\n";
}

