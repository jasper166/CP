const int INF = 1e9;
const int MOD = 1e9 + 7;
const int MAX = 5e4 + 5;

int n, k;
ll ans;
vector <int> adj[MAX];
ll dp[MAX][505];

void dfs(int u, int p){
    dp[u][0] = 1;
    for (int v : adj[u]){
        if (v == p) continue;
        dfs(v, u);
        for (int i = 0; i < k; i++)
            ans += dp[u][i] * dp[v][k - i - 1];
        for (int i = 1; i <= k; i++)
            dp[u][i] += dp[v][i - 1];
    }
}

void run_case(){
    cin >> n >> k;
    for (int i = 1; i < n; i++){
        int u, v;
        cin >> u >> v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    dfs(1, -1);
    cout << ans << "\n";
}

